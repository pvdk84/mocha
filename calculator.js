"use strict"

function addition(numberA, numberB) {
  return numberA + numberB;
}

function subtraction(numberA, numberB) {
  return numberA - numberB;
}

function multiplication(numberA, numberB) {
  return numberA * numberB;
}

function division(numberA, numberB) {
  return numberA / numberB;
}

function exponentiation(number, power) {
  return number**power;
}
